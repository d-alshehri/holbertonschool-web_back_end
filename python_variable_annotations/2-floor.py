#!/usr/bin/env python3
"""
Module 2-floor
Provides a function that returns the floor of a float.
"""


import math


def floor(n: float) -> int:
    """
    Returns the floor of a float number.

    Args:
        n (float): The number to apply floor to.

    Returns:
        int: The floor of the input float.
    """
    return math.floor(n)
