#!/usr/bin/env python3
"""
Module 0-add
This module provides a function to add two float numbers.
"""

def add(a: float, b: float) -> float:
    """
    Adds two float numbers and returns their sum as a float.

    Args:
        a (float): First number to add.
        b (float): Second number to add.

    Returns:
        float: The sum of a and b.
    """
    return a + b
